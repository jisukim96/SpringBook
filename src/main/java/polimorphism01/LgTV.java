package polimorphism01;

public class LgTV {

	public void turnOn() {
		System.out.println("LgTV -- 전원을 켭니다.");
	}
	public void turnOff() {
		System.out.println("LgTV -- 전원을 끕니다.");
	}
	public void SoundUp() {
		System.out.println("LgTV -- 볼륨을 높입니다.");
	}
	public void SoundDown() {
		System.out.println("LgTV -- 볼륨을 낮춥니다.");
	}
}
